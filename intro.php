    Users can access bibliographic details of the library resources through OPAC anywhere, thus providing 24 hours access a day. 
   The Library consists of latest collection of textbooks as well as reference books (approx. 52500), national as well as international peer reviewed journals, magazines and electronic resources on subject areas 
